app.factory('WorkdaysFactory', function($http){
    var factory = {};           

    factory.get_work_days = function(data){
        var promise = $http({
            url:'./FUNCTIONS/Workdays/get_work_days.php',
            method: 'POST',
            headers: {'Content-Type': 'application/x-www-form-urlencoded'},
            transformRequest: function(obj) {
                var str = [];
                for(var p in obj)
                str.push(encodeURIComponent(p) + "=" + encodeURIComponent(obj[p]));
                return str.join("&");
            },
            data : data
        })

        return promise;
    };

    factory.save = function(data){
        var promise = $http({
            url:'./FUNCTIONS/Workdays/save_work_days.php',
            method: 'POST',
            headers: {'Content-Type': 'application/x-www-form-urlencoded'},
            transformRequest: function(obj) {
                var str = [];
                for(var p in obj)
                str.push(encodeURIComponent(p) + "=" + encodeURIComponent(obj[p]));
                return str.join("&");
            },
            data : data
        })

        return promise;
    }; 
    
    return factory;
});